#define MAXN 100005
vll spf(MAXN, 0);
void sieve() {
    spf[1] = 1;
    for (ll i = 2; i < MAXN; i += 2) spf[i] = 2;

    for (ll i = 3; i * i < MAXN; i++) {
        if (!spf[i]) {
            for (ll j = i * i; j < MAXN; j += i) {
                if (!spf[j]) spf[j] = i;
            }
        }
    }
    for (ll i = 0; i < MAXN; i++) {
        if (!spf[i]) spf[i] = i;
    }
}
vll getPrimeFactors(ll n) {
    vll res;
    while (n > 1) {
        res.push_back(spf[n]);
        ll x = spf[n];
        while (n % x == 0) {
            n /= x;
        }
    }
    return res;
}
